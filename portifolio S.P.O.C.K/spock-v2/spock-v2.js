// Evento de hover para mostrar o texto nas imagens
// imagem 1
document.getElementById("imagem-linha2-1").addEventListener("mouseenter", ()=>{
    document.getElementById("descricao-img-1").style.display = "block";
})

document.getElementById("imagem-linha2-1").addEventListener("mouseleave", ()=>{
    document.getElementById("descricao-img-1").style.display = "none";
})

